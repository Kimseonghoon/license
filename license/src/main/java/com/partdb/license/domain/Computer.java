package com.partdb.license.domain;

public class Computer {
	private String comp_id;
	private String comp_name;
	private String comp_type;
	private String comp_user;
	private String comp_location;
	private String comp_os_lic;
	private String comp_office_lic;
	private String comp_description;	
	
	public String getComp_id() {
		return comp_id;
	}
	public void setComp_id(String comp_id) {
		this.comp_id = comp_id;
	}
	public String getComp_name() {
		return comp_name;
	}
	public void setComp_name(String comp_name) {
		this.comp_name = comp_name;
	}
	public String getComp_type() {
		return comp_type;
	}
	public void setComp_type(String comp_type) {
		this.comp_type = comp_type;
	}
	public String getComp_user() {
		return comp_user;
	}
	public void setComp_user(String comp_user) {
		this.comp_user = comp_user;
	}
	public String getComp_location() {
		return comp_location;
	}
	public void setComp_location(String comp_location) {
		this.comp_location = comp_location;
	}
	public String getComp_os_lic() {
		return comp_os_lic;
	}
	public void setComp_os_lic(String comp_os_lic) {
		this.comp_os_lic = comp_os_lic;
	}
	public String getComp_office_lic() {
		return comp_office_lic;
	}
	public void setComp_office_lic(String comp_office_lic) {
		this.comp_office_lic = comp_office_lic;
	}
	public String getComp_description() {
		return comp_description;
	}
	public void setComp_description(String comp_description) {
		this.comp_description = comp_description;
	}
	
}
